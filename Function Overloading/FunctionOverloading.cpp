int Add2Int(int n1, int n2)         // Not overloaded - different function names
{
    return n1 + n2;
}

int Add2Float(float n1, float n2)   // Not overloaded - different function names
{
    return n1 + n2;
}

int Add2Numbers(int n1, int n2)     // Overloaded - same function names
{
    return n1 + n2;
}

int Add2Numbers(float n1, float n2) // Overloaded - same function names
{
    return n1 + n2;
}
